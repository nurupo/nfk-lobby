#include "chattabtree.hpp"

namespace Chat {

TabTree::TabTree(QWidget *parent, int startingSize) :
    QTreeWidget(parent)
{
    this->startingSize = startingSize;
}

QSize TabTree::sizeHint() const
{
    return QSize(startingSize, startingSize);
}

} // namespace Chat
